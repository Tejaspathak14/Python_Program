my_integer = 42
print("Integer:", my_integer, "- Type:", type(my_integer))

my_float = 3.14159
print("Float:", my_float, "- Type:", type(my_float))

my_string = "Hello, World!"
print("String:", my_string, "- Type:", type(my_string))

my_boolean = True
print("Boolean:", my_boolean, "- Type:", type(my_boolean))

is_python_fun = False
print("Another Boolean:", is_python_fun, "- Type:", type(is_python_fun))

my_variable = 10
print("Initial variable:", my_variable, "- Type:", type(my_variable))
my_variable = "Now a string"
print("Reassigned variable:", my_variable, "- Type:", type(my_variable))
